const TIME_MINUTES: Record<string, number> = {
  '15分': 15, '30分': 30, '1時間': 60, '1.5時間': 90,
  '2時間': 120, '3時間': 180, '半日': 240, '1日': 480,
};

function buildHistoryText(history: TaskHistory[]): string {
  if (history.length === 0) return '履歴なし（初回のタスク指示）';
  return history.map(h =>
    `・${h.実施日} ｜ ${h.タスク名} ｜ カテゴリ:${h.カテゴリ} ｜ 状態:${h.状態} ｜ 評価:${h.評価}` +
    (h.上司コメント ? ` ｜ 上司コメント:${h.上司コメント}` : '') +
    (h.次回への申し送り ? `\n  → 申し送り:${h.次回への申し送り}` : '')
  ).join('\n');
}

