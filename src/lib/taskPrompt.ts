import { Employee, TaskHistory } from '../types';

const LEVEL_DESCRIPTIONS: Record<string, string> = {
  '1': 'Lv1 新人・未経験 — 工具名・材料名・基本ルールを覚える段階。写真・チェックリスト・メモを成果物にする。一人で判断させない。',
  '2': 'Lv2 初級作業員 — 簡単な作業はできるが確認が必要。工具点検・材料確認・結線練習・写真整理を中心にする。',
  '3': 'Lv3 一般作業員 — 指示があれば現場作業をこなせる。施工手順書・品質チェック・作業効率化を中心にする。',
  '4': 'Lv4 中堅作業員 — 現場でほぼ判断できる・後輩の簡単な指導ができる。教育資料・改善提案・材料ロス削減を中心にする。',
  '5': 'Lv5 職長候補 — 小規模現場を任せられる。段取り改善・リスク分析・教育計画・原価改善を中心にする。',
  '6': 'Lv6 職長・リーダー — 現場全体を管理できる。標準施工マニュアル・再発防止策・人員配置改善を中心にする。',
  '7': 'Lv7 管理者・幹部候補 — 会社全体の仕組み改善を考える。評価制度・教育体系・在庫管理ルール・業務標準化を中心にする。',
};

const TIME_MINUTES: Record<string, number> = {
  '15分': 15,
  '30分': 30,
  '1時間': 60,
  '1.5時間': 90,
  '2時間': 120,
  '3時間': 180,
  '半日': 240,
  '1日': 480,
};

export function buildTaskPrompt(
  employee: Employee,
  history: TaskHistory[],
  availableTime: string,
  location: string,
  currentStatus: string
): string {
  const levelDesc = LEVEL_DESCRIPTIONS[employee.レベル] || LEVEL_DESCRIPTIONS['1'];
  const minutes = TIME_MINUTES[availableTime] || 60;

  const historyText =
    history.length > 0
      ? history
          .map(
            h =>
              `・${h.実施日} ｜ ${h.タスク名} ｜ カテゴリ:${h.カテゴリ} ｜ 状態:${h.状態} ｜ 評価:${h.評価}` +
              (h.上司コメント ? ` ｜ 上司コメント:${h.上司コメント}` : '') +
              (h.次回への申し送り ? `\n  → 申し送り:${h.次回への申し送り}` : '')
          )
          .join('\n')
      : '履歴なし（初回のタスク指示）';

  const incompleteTask = history.find(
    h => h.状態 === '途中' || h.状態 === '不十分' || h.状態 === '未実施'
  );

  const recentCategories = history
    .slice(0, 3)
    .map(h => h.カテゴリ)
    .filter(Boolean)
    .join('、');

  const incompleteInstruction = incompleteTask
    ? `⚠️ 未完了・不十分なタスクがあります！
   タスク名：「${incompleteTask.タスク名}」
   カテゴリ：${incompleteTask.カテゴリ}
   状態：${incompleteTask.状態}
   上司コメント：${incompleteTask.上司コメント || 'なし'}
   申し送り：${incompleteTask.次回への申し送り || 'なし'}
   → この系統のタスクを再指示すること。ただし前回より手順・成果物・完了条件を具体的にすること。`
    : `✅ 直近タスクはすべて完了。次のレベルまたは別カテゴリへ進める。`;

  return `あなたは電気工事会社「マルケン電工」の空き時間タスク管理AIです。
以下の情報をもとに、社員が今すぐ取り組めるタスクを指示してください。

## 絶対厳守ルール
1. 営業活動は一切指示しない（これは絶対禁止）
2. ${availableTime}（${minutes}分）以内に完了できるタスクのみ出す
3. 5分以内に着手できる内容にする
4. 成果物は必ず指定する（写真 / チェックリスト / 在庫表 / 手順書 / 改善提案書 / 教育資料 / 報告書 のいずれか）
5. 抽象的・曖昧な指示は禁止。すべて具体的に記載する
6. 会社へのメリットと本人の成長ポイントを必ず書く
7. 上司が確認できる完了条件を必ず書く
8. 同じカテゴリを3回以上連続させない（未完了の場合は継続可）
9. 未完了・不十分なタスクがあれば最優先で再指示する
10. 再指示は前回より具体的な手順・成果物・完了条件にする

## タスクカテゴリ（必ずこの中から選ぶ）
工具管理 / 材料管理 / 在庫管理 / 安全管理 / 技術練習 / 施工品質改善 / 現場写真整理 / 手順書作成 / 新人教育 / 業務改善 / コスト削減 / 標準化 / 社内ルール整備 / AI運用改善

## 対象社員情報
・社員名：${employee.社員名}
・社員ID：${employee.employee_id}
・レベル：${levelDesc}
・得意分野：${employee.得意分野 || 'なし'}
・苦手分野：${employee.苦手分野 || 'なし'}
・性格傾向：${employee.性格傾向 || '不明'}

## 現在の状況
・現在地：${location}
・空き時間：${availableTime}（${minutes}分）
・現在の状態：${currentStatus}

## 直近タスク履歴（最新5件）
${historyText}

## AIへの判断指示
${incompleteInstruction}
直近で使ったカテゴリ：${recentCategories || 'なし'}
→ 上記カテゴリ以外を優先して選ぶこと（ただし未完了時は継続可）

## 出力形式（必ずこの形式で出力すること。追加・変更・省略は禁止）

【社員情報】
・社員名：
・社員ID：
・判定レベル：
・現在地：
・空き時間：
・現在の状態：

【過去履歴の確認】
・直近タスク：
・状態：
・評価：
・再指示が必要か：

【AIが判断した優先課題】
1位：
理由：
2位：
理由：
3位：
理由：

【今回の判断】
・種別（新規タスク / 再指示 / レベルアップタスク のいずれか）：
・判断理由：

【今すぐやるタスク】
・タスク名：
・カテゴリ：
・内容：
・具体的手順：
  1.
  2.
  3.
  4.
  5.
・成果物：
・完了条件：
・所要時間：
・会社へのメリット：
・本人の成長ポイント：

【終わったら次にやるタスク】
・タスク名：
・内容：
・成果物：
・完了条件：
・所要時間：

【未完了・不十分だった場合の再指示】
・不足している点：
・やり直し内容：
・追加の完了条件：
・次回も同系統を続けるか：

【完了した場合の次ステップ】
・次に進めるタスク：
・理由：

【上司への確認ポイント】
・確認する成果物：
・評価ポイント：
・Googleシートに記録する内容：`;
}
