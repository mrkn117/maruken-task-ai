// 時間→分変換
// ─────────────────────────────────────────────────────────────────────────────
const TIME_TO_MINUTES: Record<string, number> = {
  '15分': 15, '30分': 30, '1時間': 60, '1.5時間': 90,
  '2時間': 120, '3時間': 180, '半日': 240, '1日': 480,
};

// ─────────────────────────────────────────────────────────────────────────────
// タスク選択ロジック
// ─────────────────────────────────────────────────────────────────────────────
function selectTask(
  employee: Employee,
  history: TaskHistory[],
  availableTime: string,
  location: string
): TaskTemplate {
  const level = parseInt(employee.レベル) || 1;
  const maxMinutes = TIME_TO_MINUTES[availableTime] || 60;
  const recentCategories = history.slice(0, 3).map(h => h.カテゴリ);
  const incompleteTasks = history.filter(h => h.状態 === '途中' || h.状態 === '不十分');

  // 未完了タスクがあれば同カテゴリを優先
  if (incompleteTasks.length > 0) {
    const target = incompleteTasks[0];
    const sameCategory = TASK_TEMPLATES.filter(t =>
      t.category === target.カテゴリ &&
      level >= t.minLevel && level <= t.maxLevel &&
      t.durationMin <= maxMinutes &&
      (!t.validLocations || t.validLocations.includes(location))
    );
    if (sameCategory.length > 0) {
      return sameCategory[Math.floor(Math.random() * sameCategory.length)];
    }
  }

  // 通常選択：レベル・時間・場所でフィルタし、最近使っていないカテゴリを優先
  const candidates = TASK_TEMPLATES.filter(t =>
    level >= t.minLevel &&
    level <= t.maxLevel &&
    t.durationMin <= maxMinutes &&
    (!t.validLocations || t.validLocations.includes(location))
  );

  // 最近使っていないカテゴリを優先
  const fresh = candidates.filter(t => !recentCategories.includes(t.category));
  const pool = fresh.length > 0 ? fresh : candidates;

  if (pool.length === 0) {
    // フォールバック：制約を緩めて選択
    return TASK_TEMPLATES[Math.floor(Math.random() * TASK_TEMPLATES.length)];
  }

  return pool[Math.floor(Math.random() * pool.length)];
}

// ─────────────────────────────────────────────────────────────────────────────
// AI出力形式でフォーマット
// ─────────────────────────────────────────────────────────────────────────────
function formatOutput(
  template: TaskTemplate,
  employee: Employee,
  history: TaskHistory[],
  availableTime: string,
  location: string,
  currentStatus: string
): string {
  const level = parseInt(employee.レベル) || 1;
  const levelNames: Record<number, string> = {
    1: 'Lv1 新人・未経験', 2: 'Lv2 初級作業員', 3: 'Lv3 一般作業員',
    4: 'Lv4 中堅作業員', 5: 'Lv5 職長候補', 6: 'Lv6 職長・リーダー', 7: 'Lv7 管理者・幹部候補',
  };

  const lastTask = history[0];
  const incompleteTasks = history.filter(h => h.状態 === '途中' || h.状態 === '不十分');
  const isReinstructing = incompleteTasks.length > 0 && incompleteTasks[0].カテゴリ === template.category;

  const _cats = history.slice(0, 3).map(h => h.カテゴリ).filter(Boolean);
  const recentCategories = _cats.filter((c, i) => _cats.indexOf(c) === i);
  const priorities = [
    template.category,
    ...TASK_TEMPLATES
      .filter(t => t.category !== template.category && parseInt(employee.レベル) >= t.minLevel && !recentCategories.includes(t.category))
      .map(t => t.category)
      .filter((c, i, a) => a.indexOf(c) === i)
      .slice(0, 2),
  ];

  // ステップに時間配分を付ける（合計がdurationMinに近くなるよう均等割り）
  const stepMin = Math.round(template.durationMin / template.steps.length);
  const stepsText = template.steps.map((s, i) => {
    // すでに【XX分】が含まれている場合はそのまま使う
    if (/【\d+分】/.test(s)) return `  ${i + 1}. ${s}`;
    return `  ${i + 1}. 【${stepMin}分】${s}`;
  }).join('\n');

  const judgeType = isReinstructing ? '再指示' : level >= 5 ? 'レベルアップタスク' : '新規タスク';
  const judgeReason = isReinstructing
    ? `前回の「${incompleteTasks[0].タスク名}」が${incompleteTasks[0].状態}のため、同カテゴリで具体的な再指示を出します`
    : recentCategories.length > 0
      ? `直近は${recentCategories.join('・')}を実施済みのため、${template.category}カテゴリに進みます`
      : '初回または全カテゴリを網羅的に進めるため、このタスクを選択しました';

  const nextTaskName = template.nextTaskName || `${priorities[1] || '安全管理'}カテゴリの次のタスク`;
  const nextTaskContent = template.nextTaskContent || 'このタスクが完了したら上司に報告し、次の空き時間タスクを確認する';

  return `【社員情報】
・社員名：${employee.社員名}
・社員ID：${employee.employee_id}
・判定レベル：${levelNames[level] || `Lv${level}`}
・現在地：${location}
・空き時間：${availableTime}
・現在の状態：${currentStatus}

【過去履歴の確認】
・直近タスク：${lastTask ? `${lastTask.タスク名}（${lastTask.実施日}）` : 'なし（初回）'}
・状態：${lastTask?.状態 || '－'}
・評価：${lastTask?.評価 || '－'}
・再指示が必要か：${isReinstructing ? `はい（${incompleteTasks[0].タスク名}が${incompleteTasks[0].状態}のため）` : 'いいえ'}

【AIが判断した優先課題】
1位：${priorities[0] || template.category}
理由（会社利益・スキルへの具体的インパクト）：${template.companyBenefit}
2位：${priorities[1] || '安全管理'}
理由：安全管理の継続的な強化は労災ゼロと法的リスク低減に直結します
3位：${priorities[2] || '業務改善'}
理由：業務プロセスの改善は1件あたりの工事利益率を高める基盤になります

【今回の判断】
・種別（新規タスク / 再指示 / レベルアップタスク のいずれか）：${judgeType}
・判断理由：${judgeReason}
・このタスクを選んだ理由（他の選択肢より優れている点）：${availableTime}という時間枠で完結でき、${location}で今すぐ着手できる。成果物が明確で上司が評価しやすい。

【今すぐやるタスク】
・タスク名：${template.name}
・カテゴリ：${template.category}
・内容（2〜3文で背景と目的を説明）：${template.content}。このタスクを実施することで${template.category}の課題を具体的に改善し、会社と個人の双方に価値をもたらします。
・具体的手順（各ステップに【XX分】の時間配分を必ず記載）：
${stepsText}
・成果物（何が完成するか具体的に）：${template.artifact}
・完了条件（上司が30秒で合否判定できる基準）：${template.completionCondition}
・所要時間：約${template.durationMin}分
・会社へのメリット（金額・時間・品質の数値推定を含める）：${template.companyBenefit}
・本人の成長ポイント（このタスクで何ができるようになるか具体的に）：${template.growthPoint}
・よくあるミスと対策：途中で作業が中断した場合は必ずどこまで完了したか記録を残すこと。成果物の品質を焦って下げないこと。わからない点は作業を止めて上司に確認すること。
・このタスクが会社利益に直結する理由：${template.category}の改善は現場でのトラブル防止・品質向上・教育コスト削減のいずれかに貢献し、会社の粗利率改善につながります。

【終わったら次にやるタスク】
・タスク名：${nextTaskName}
・内容：${nextTaskContent}
・成果物：完了報告メモまたは次タスクの準備メモ
・完了条件：上司に今日のタスク成果を報告済みで、次の行動が決まっている
・所要時間：5〜10分
・スキルアップのポイント：報告の習慣化と次のアクションを自分で考える力が身につく

【未完了・不十分だった場合の再指示】
・具体的に不足している部分：成果物（${template.artifact}）が未完成、または品質・記載内容が不十分
・やり直し内容（前回より具体的に）：手順を最初から見直し、特に「成果物の品質基準」を確認してから作業する。わからない部分は上司に質問してから進める。
・追加の完了条件：上司が成果物を見て「次の現場で使える」「後輩に渡せる」と判断できること
・次回も同系統を続けるか（理由付きで）：はい（${template.category}カテゴリを継続。完了品質に達するまで同カテゴリを繰り返すことで本物のスキルが身につく）

【完了した場合の次ステップ】
・次に進めるタスク（具体的なタスク名）：${nextTaskName}
・理由（なぜこのタスクが次に最適か）：${template.category}カテゴリの基礎が固まったため、関連する別カテゴリに展開してスキルの幅を広げます
・レベルアップへの道筋：各カテゴリのタスクを「完了」評価で積み上げることでLv${Math.min(level + 1, 7)}への昇格条件を満たしていきます

【上司への確認ポイント】
・確認する成果物（具体的に何を見せるか）：${template.artifact}（完成したもの全て）
・評価ポイント（何を見て合格とするか）：①成果物の完成度と正確さ ②所要時間内に完了したか ③次の現場・業務で使えるレベルか
・このタスクが会社全体の改善につながる説明（上司への報告文案）：「本日の空き時間に${template.category}タスク「${template.name}」を実施しました。${template.companyBenefit}。成果物をご確認ください。」`;
}

// ─────────────────────────────────────────────────────────────────────────────
// 公開API
// ─────────────────────────────────────────────────────────────────────────────
export function generateTaskLocally(
  employee: Employee,
  history: TaskHistory[],
  availableTime: string,
  location: string,
  currentStatus: string
): string {
  const template = selectTask(employee, history, availableTime, location);
  return formatOutput(template, employee, history, availableTime, location, currentStatus);
}
